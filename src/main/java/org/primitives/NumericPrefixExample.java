package org.primitives;

public class NumericPrefixExample {
    public static void main(String[] args) {
        // Binary literal (base 2) with prefix 0b
        int binaryNum = 0b1010; // 10 in decimal
        System.out.println(binaryNum);

        // Octal literal (base 8) with prefix 0
        int octalNum = 012; // 10 in decimal
        System.out.println(octalNum);

        // Hexadecimal literal (base 16) with prefix 0x
        int hexNum = 0x1A; // 26 in decimal
        System.out.println(hexNum);

    }
}
